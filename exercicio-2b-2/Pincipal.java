    private void buttonSalvarActionPerformed(java.awt.event.ActionEvent evt) {                                             
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) tableDados.getModel();
        int rowSelected = tableDados.getSelectedRow();
        String nome, dtNasc, tel, end;
        nome=inputNome.getText();
        dtNasc=inputDtNasc.getText();
        tel=inputTel.getText();
        end=inputEnd.getText();
        if(rowSelected == -1){
            comportamentoAdd();
        }
        else{
            dtm.setValueAt(nome, rowSelected, 0);
            dtm.setValueAt(dtNasc, rowSelected, 1);
            dtm.setValueAt(tel, rowSelected, 2);
            dtm.setValueAt(end, rowSelected, 3);  
            EntityManagerFactory ponte = PersistenceSingleton.getInstance().getEntityManagerFactory();
            
            AndarilhoJpaController andarilhoController = new AndarilhoJpaController(ponte);

            Andarilho usu1 = new Andarilho();
            usu1.setNome(nome);
            usu1.setDtNasc(dtNasc);
            usu1.setTelefone(tel);
            usu1.setEndereco(end);
            
            try{
                andarilhoController.edit(usu1);
                
            }
            catch(NonexistentEntityException e){
                System.out.println("Erro! ID inexistente!");
            }
            catch(Exception e){
                System.out.println("Erro!");
            }
        }
        
    }                                            

