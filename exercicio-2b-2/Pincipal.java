    private void buttonRemoverActionPerformed(java.awt.event.ActionEvent evt) {                                              
        // TODO add your handling code here:
        DefaultTableModel dtm=(DefaultTableModel) tableDados.getModel();
        int rowSelected=tableDados.getSelectedRow();
        
            if(rowSelected==-1){
               JOptionPane.showMessageDialog(this, "Selecione a linha que quer remover antes de clicar no botão!");
            }
            else{
                
                int opcao = JOptionPane.showConfirmDialog(this,"Tem certeza que deseja remover a linha selecionada?","Confirme sua operação", JOptionPane.YES_NO_OPTION);
                
                if (opcao==JOptionPane.YES_OPTION)
                    dtm.removeRow(rowSelected);
            }
    }                                             

    public void comportamentoAdd(){
        String nome, dataNascimento, telefone, endereco;
        nome=inputNome.getText();
        dataNascimento=inputDtNasc.getText();
        telefone=inputTel.getText();
        endereco=inputEnd.getText();
        if(nome.equals("") || dataNascimento.equals("") || telefone.equals("") || endereco.equals("")){
            JOptionPane.showMessageDialog(this, "Campo Nome, Data Nascimento, Telefone e Endereço são de preenchimentos obrigatórios");
        }
        else{
            DefaultTableModel dtm= (DefaultTableModel) tableDados.getModel();
            String a []= new String[] {nome,dataNascimento,telefone,endereco};
            dtm.addRow(a);
        
            EntityManagerFactory ponte = PersistenceSingleton.getInstance().getEntityManagerFactory();
            
            
            AndarilhoJpaController andarilhoController = new AndarilhoJpaController(ponte);
            
            Andarilho usu1 = new Andarilho();
            usu1.setNome(nome);
            usu1.setDtNasc(dataNascimento);
            usu1.setEndereco(endereco);
            usu1.setTelefone(telefone);
            andarilhoController.create(usu1);
            
            System.out.println(usu1.toString());
        }
    }
    
